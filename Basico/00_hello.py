#Type es una función que nos dice que tipo de dato es
print(type("Hola amigo")) #Tipo str
print(type(5)) #Tipo int
print(type(1.5)) #Tipo float
print(type(True)) #Tipo booleano
print(type(1 + 2j)) #Tipo complejo para matematicos
print(type(['banana', 'limon', 'apio'])) #Tipo lista
print(type({'name': 'Ana'})) #Tipo Diccionario
print(type({9.8, 3.14, 2.7})) #Tipo Set
print(type((1,2,3,4))) #Tipo tupla

